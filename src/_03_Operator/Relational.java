package _03_Operator;

//Relational Operator =(== != < > <= >=)

public class Relational {
	public static void main(String[] arg)
	{
		int a=100;
		int b=80;
		
		System.out.println( a == b);
		System.out.println( a != b );
		System.out.println( a < b );
		System.out.println( a > b );
		System.out.println( a <= b );
		System.out.println( a >= b );
		
		
	}

}
