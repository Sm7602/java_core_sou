package _03_Operator;

//Increment Operator =(++)
//Decrement Operator =(--)

public class Incremental {
   public static void main(String[] arg)
   {
	   int a=100;
	   
	   //pre-incremental
	   System.out.println(a++);
	   //post-incrimental
	   System.out.println(++a);
	   
	   System.out.println(a);
	   
	   //pre-dicremental
	   System.out.println(a--);
	   //post-dicremental
	   System.out.println(--a);
	   
	   System.out.println(a);
	   
	   
   }
}
